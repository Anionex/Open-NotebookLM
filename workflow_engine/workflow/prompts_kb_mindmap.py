from __future__ import annotations
from typing import List, Dict


def _build_single_pass_prompt(contents_str: str, language: str, max_depth: int) -> str:
    """单次生成的原始 Prompt（短文本路径）。"""
    return f"""请基于以下文档内容，生成一张层级清晰、主题归纳明确、适合快速把握全文结构的思维导图。你的目标是把文章压缩成一张"知识地图"，让读者能够迅速理解这篇文章的核心主题、主要模块、关键内容，以及各部分之间的层级关系。

请按照下面的方法完成：

先通读全文，识别文章真正的中心主题，用一句高度概括的话作为根节点。这个根节点需要能够覆盖全文主旨，而不是局部内容。输出时将根节点作为**一级标题（#）**呈现。

然后围绕中心主题，提炼出最能代表全文结构的主分支，优先控制在 4–8 个范围内，并将它们作为**二级标题（##）**呈现。主分支要能够代表全文最重要的几个结构板块。请优先从文章本身的结构、论述重心、主题模块、关键信息簇中提炼主分支，使它们能够共同构成全文的主干。

接着在每个主分支下继续拆分 2–4 个子主题，并将它们作为**三级标题（###）**呈现。子主题要承接上级主题，概括该部分最值得保留的核心内容，例如关键概念、主要论点、重要机制、核心步骤、代表性案例、主要证据、结果表现、应用场景、影响因素、行动建议等。

需要时继续向下拆分更细的层级，整体层级最多{max_depth}层（使用 # 到 {'#' * max_depth} 表示）。请根据内容复杂度自然决定展开深度，使层级既完整又紧凑。

**语言要求（必须严格遵守）**：所有输出节点的文字必须使用 **{language}** 语言。无论本提示使用什么语言书写，你输出的思维导图节点内容必须全部使用 {language}。

整张思维导图要体现出一种知识整理型、结构概览型、主题分解型的风格。重点是帮助读者快速理解全文内容版图，而不是只罗列摘要句子。请让结构体现出"从总到分、由主到次、层层展开"的组织逻辑。

请根据文章类型，自适应选择最合适的组织框架：

研究型、分析型文章：背景、问题、核心观点、方法机制、证据结果、意义影响、局限延伸
新闻、时评、纪实型文章：主题事件、背景脉络、关键事实、各方观点、原因影响、趋势走向
科普、说明型文章：核心概念、原理机制、特征表现、应用场景、常见理解、总结启发
教程、方法型文章：目标、前提条件、步骤模块、关键技巧、注意事项、应用建议
商业、产品、行业文章：对象定位、背景环境、核心策略、运行方式、优势价值、风险挑战、发展趋势
访谈、观点型文章：核心立场、主要观点、支撑理由、经验案例、延伸启示

节点命名规则（严格遵守）：
1. 每个节点用 3–12 个字的短语命名，**禁止在节点中使用括号补充说明**
2. 如果需要补充数据或细节，请将其作为下一级子节点单独展开，而不是用括号附在当前节点后
3. 错误示例：`### Grover's Algorithm (O(√n) queries)` — 括号内容应拆分为子节点
4. 正确示例：`### Grover算法` 下设 `#### 时间复杂度 O(√n)`

请将语义接近、作用相同、共同服务于同一主题的信息归并到同一分支下，形成稳定的主题模块。请优先把能够解释全文结构的内容放在上层节点，把例子、数据、条件、补充说明放在下层节点。

特别注意：如果原文包含关键数据、时间、比例、数量、金额、指标或实验结果，请在相关节点中保留这些具体信息，但以子节点形式呈现，而非括号内联。

如果输入中包含多份文档，必须均衡覆盖每份文档的核心内容，不得忽略任何一份文档。每份文档至少应有 1-2 个主分支覆盖其独有的主题。

生成标准：

1. 忠实反映原文主线，保留关键数据和定量结果（以子节点形式）
2. 节点之间要有明确层级关系
3. **分支均衡性硬约束**：每个主分支（##）下的总节点数（包括所有子层级）应控制在 10-25 个之间。如果某个主题预计会产生超过 25 个节点，必须将它拆分为 2-3 个更具体的独立主分支。例如"伦理、风险与监管"应拆分为"伦理争议"、"安全风险"、"政策监管"三个独立主分支
4. **全文覆盖**：不能遗漏文章的任何重要章节或主题板块。先通读全文，识别所有不同的主题领域，确保每个主题领域至少有一个主分支覆盖。特别注意文章末尾的总结、展望、哲学思考等部分不要被忽略
5. 优先保留能够帮助理解全文框架的内容
6. 让整张图看起来像一张"文章结构与知识重点总览图"
7. 读者即使不读原文，也能通过这张导图把握文章的大体结构与核心内容
8. 主分支数量硬性要求 5–8 个，不可少于 5 个，不可多于 8 个

输出格式要求：

1. 使用 Markdown 标题格式：# 根节点，## 主分支，### 子主题，#### 及以下为进一步展开层级
2. 最终结果直接以 Markdown 主体输出，内容从根节点开始展开
3. 采用纯 Markdown 标题结构呈现结果

文档内容：
{contents_str}

请严格按照文档内容前的要求来完成，特别注意：
1. 采用纯 Markdown 标题结构（# ## ### ####），不要使用代码块或其他格式
2. 保留原文中的关键数据、数字和定量信息，以子节点形式呈现
3. 节点简洁有信息量，用短语式表达而非长句，**禁止括号内联**
4. 层级清晰，从总到分、由主到次
5. 主分支必须恰好 5–8 个，每个主分支下的总节点数控制在 10-25 个，超过则拆分为独立主分支
6. 覆盖文章所有重要主题板块，不可遗漏文末的总结、展望、哲学思考等内容
7. **输出语言必须是 {language}**，不可使用其他语言
8. 不要输出任何解释或额外文字，直接从根节点开始输出："""


def _build_map_prompt(chunk: Dict, language: str) -> str:
    """Map 阶段 Prompt：从单个 chunk 提取 {summary, nodes}。

    importance_score 为**段内相对分**（1=本片段细节，5=本片段核心），不跨片段可比。
    """
    chunk_id = chunk["chunk_id"]
    source = chunk["source"]
    text = chunk["text"]

    lang_instruction = "使用中文" if language == "zh" else f"Use {language} language"

    return f"""你是一位资深知识提炼专家。请阅读以下文本片段，先写一段片段摘要，再提取该片段的核心知识点构成结构化节点列表。

## 重要度评分（段内相对分，仅反映该节点在本片段中的重要性，不比较跨片段）
- **5分**：本片段的核心主题或关键论点
- **4分**：本片段的主干概念、重要机制、关键结论
- **3分**：本片段中有价值的支撑论据、重要子概念、关键数据
- **2分**：本片段的补充说明、次要例子、背景信息
- **1分**：本片段的细枝末节、过渡性文字、重复信息

## 提取要求
1. 先用 2-4 句话写一段片段摘要（summary 字段），覆盖该片段的主旨与要点
2. 提取 8-20 个知识节点（严禁超过 20 个）。聚焦本片段最核心的主题和关键信息，不要为每个细节都创建节点
3. 节点 topic 使用简洁短语（3-12 字），适合放入思维导图。禁止在 topic 中使用括号补说
4. 正确设置 parent_topic 反映层级关系：最顶层节点设为 "ROOT"，子节点指向其上层 topic 字符串
5. **定量数据强制保留**：凡是原文中出现的具体数字、年份、百分比、金额、数量、实验数据，必须作为独立节点或写入 summary。含定量数据的节点 importance_score 至少为 3
6. {lang_instruction}

## 输出格式（仅输出合法 JSON 对象，不要包含任何解释文字或 Markdown 围栏）
{{
  "summary": "2-4 句话的片段摘要",
  "nodes": [
    {{
      "node_id": "{chunk_id}_n0",
      "topic": "节点主题（简洁短语）",
      "parent_topic": "ROOT",
      "summary": "1-2 句话概括该知识点的核心内容",
      "importance_score": 5,
      "source_chunk_id": "{chunk_id}"
    }}
  ]
}}

## 文本片段（来自: {source}，片段ID: {chunk_id}）
---
{text}
---

请直接输出 JSON 对象："""


def _build_collapse_prompt(group_a_json: str, group_b_json: str, language: str) -> str:
    """Collapse 阶段 Prompt：软合并两组知识节点，不强制剪枝比例。"""
    lang_instruction = "使用中文输出" if language == "zh" else f"Output in {language}"

    return f"""你是一位知识结构整合专家。请将以下两组从相邻片段中提取的知识节点合并去重，得到一组规模更紧凑的节点列表。

## 合并原则
1. **只合并真正冗余的节点**：topic 相同、含义高度相似或属于同一事物的不同表述；语义不同的节点必须独立保留
2. **以高分节点为主导**：当两个节点可合并时，保留 importance_score 较高者作为"主节点"，将另一节点的独特信息融入其 summary；合并后的分数取原两者中的较高分
3. **保留独立主题**：parent_topic 为 ROOT 的顶层主题节点原则上独立保留，不与其他 ROOT 合并（除非完全同主题）
4. **定量数据不得丢失**：合并节点时，两侧 topic/summary 中出现的具体数字、百分比、金额、年份必须保留在合并后节点的 summary 中
5. **不强求剪枝比例**：如果两组之间冗余很少，可以只合并少量节点；关键是质量（准确去重），不是数量
6. **建立父子关系**：如果一个节点是另一个的子概念，正确设置 parent_topic
7. **保留 source_chunk_id**：若合并多个 chunk 的节点，source_chunk_id 取"主节点"来源；如果需要可用逗号连接多来源
8. **节点编号**：node_id 重新编为 "merged_n0", "merged_n1", ...（不再关心原编号）
9. {lang_instruction}

## 输出格式
仅输出合法的 JSON 数组，不要包含任何解释文字或 Markdown 围栏。每个节点格式：
{{"node_id": "merged_nX", "topic": "...", "parent_topic": "ROOT或上级topic", "summary": "...", "importance_score": 1-5, "source_chunk_id": "..."}}

## 节点组 A
{group_a_json}

## 节点组 B
{group_b_json}

请直接输出合并后的 JSON 数组："""


def _build_reduce_prompt(
    chunk_summaries: List[Dict],
    headings_md: str,
    retained_nodes_json: str,
    language: str,
    max_depth: int,
) -> str:
    """Reduce 阶段 Prompt：根据 chunk 摘要 + 原文标题 + 保留节点，生成 Markdown 思维导图。

    **不再按 importance_score 建层级**——层级结构参考原文标题和摘要中的自然主题。
    """
    lang_instruction = f"使用 {language} 输出" if language != "zh" else "使用中文输出"

    summary_block = "\n".join(
        f"- [{it.get('chunk_id','')}] {it.get('summary','')}"
        for it in chunk_summaries if it.get("summary")
    ) or "(无可用片段摘要)"

    headings_block = headings_md.strip() or "(原文未提供标题结构)"

    return f"""你是一位思维导图设计专家。请综合以下三类信息，生成一份层级清晰、主题归纳得当的思维导图（Markdown 标题树）。

## 输入信息 A：各片段摘要（按原文顺序，用于把握文章主干脉络）
{summary_block}

## 输入信息 B：原文标题结构（若可用，优先参考它规划主分支与层级）
{headings_block}

## 输入信息 C：保留的知识节点（去重后的要点，用于填充细节）
下方每项为一个节点：
- topic：节点主题
- parent_topic：上级节点（ROOT 表示最顶层）
- summary：内容摘要
- source_chunk_id：节点来源片段
{retained_nodes_json}

## 生成规则
1. **根节点**（# 一级标题）：用一句高度概括的短语覆盖全文主旨，不超过 15 字
2. **主分支**（## 二级标题）：恰好 5-8 个。优先参考原文标题结构（输入 B）决定主分支，其次从片段摘要（输入 A）中归纳自然主题；不要按节点分数选主分支
3. **子层级**（### 三级及以下）：参考节点之间的 parent_topic 关系和语义关联组织；不需要将每个节点都搬进导图
4. **深度使用**：整体最多 {max_depth} 层；对含定量数据或重要细节的节点向下展开
5. **命名风格**：3-12 字短语；禁止括号内联补说；若需补充数据作为下一级子节点
6. **定量数据保留**：节点 summary 中出现的数字、百分比、金额、年份需要呈现在导图中（作为独立节点或子节点）
7. **总节点数约束**：最终 80-150 个节点（包含所有层级）；以压缩结构、保留关键信息为目标，不要逐条搬运输入节点
8. **覆盖性**：确保每段摘要对应的主题都在导图中有相应分支
9. {lang_instruction}

## 输出格式
- 纯 Markdown 标题结构：# 根节点，## 主分支，### 子主题，#### 及以下
- 不要使用代码块、列表符号或其他格式
- 不要输出任何解释文字，直接从根节点开始

请直接输出思维导图："""


def _build_merge_prompt(article_markdowns: List[Dict], language: str, max_depth: int) -> str:
    """多文章合并 Prompt：将多份 markdown 思维导图合并为一份。"""
    lang_instruction = f"使用 {language} 输出" if language != "zh" else "使用中文输出"

    parts = []
    for i, item in enumerate(article_markdowns, 1):
        fn = item.get("filename", f"article_{i}")
        md = item.get("markdown", "").strip()
        parts.append(f"## 文章 {i}：{fn}\n{md}")
    combined = "\n\n".join(parts)

    return f"""你是一位思维导图整合专家。下方给出多份已生成的思维导图（Markdown 标题树），每份对应一篇文章。请将它们合并为一份更大的思维导图。

## 合并规则
1. **新根节点**（# 一级标题）：造一个能覆盖所有文章共同主旨的短语，不超过 20 字
2. **每篇文章降级为主分支**（## 二级标题）：将原文章根节点（原本的 #）改为 ## 形式，作为新根下的主分支；其下层结构（原 ## 变 ###，原 ### 变 ####……）保持原顺序与内容
3. **跨文章主题合并**：如果多篇文章在相近主题上都有分支，可以合并为单个主分支，来源于不同文章的子节点都挂在其下（适度，避免过度合并丢失差异）
4. **主分支数量**：最多 10 个主分支；文章较多时合并相近主题
5. **总节点数**：不超过 200 个节点
6. **深度上限**：{max_depth} 层
7. **命名风格统一**：3-12 字短语，禁止括号内联
8. {lang_instruction}

## 输入（多份思维导图）
{combined}

## 输出格式
- 纯 Markdown 标题结构
- 不要使用代码块或解释文字
- 直接从根节点开始

请直接输出合并后的思维导图："""


def _build_beautify_prompt(markdown: str, language: str, max_depth: int) -> str:
    """美化 Prompt：结构重平衡 + 命名优化，一次到位。"""
    lang_instruction = f"使用 {language} 输出" if language != "zh" else "使用中文输出"
    return f"""你是一位思维导图润色专家。请对下方思维导图（Markdown 标题树）做一次美化，保证整体结构更均衡、命名更简洁统一。

## 美化要求
1. **结构重平衡**：
   - 主分支（##）数量控制在 5-8 个；过多则合并相近主题，过少则拆分内容过胖的主分支
   - 单个主分支下节点总数（含所有子层级）控制在 10-25 个；过胖拆分，过瘦合并
   - 整体深度不超过 {max_depth} 层
2. **命名优化**：
   - 节点使用 3-12 字短语；删除括号补说；统一风格（例如都用名词短语）
   - 定量数据（数字、百分比、金额、年份）保留；若原本放在 topic 括号里，拆分为下级子节点
3. **信息保真**：不要删除原导图中的具体事实、数据、关键概念；只做结构与命名的改动
4. {lang_instruction}

## 输入思维导图
{markdown}

## 输出格式
- 纯 Markdown 标题结构（# ## ### ####）
- 不要使用代码块或解释文字
- 直接从根节点开始

请直接输出美化后的思维导图："""
